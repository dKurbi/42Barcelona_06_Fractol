#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (c % 256 == s[i])
			return ((char *) &s[i]);
		i--;
	}
	return (NULL);
}
