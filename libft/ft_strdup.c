#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*rtn;
	int		i;

	i = 0;
	rtn = (char *)malloc((ft_strlen(s1) + 1) * sizeof (char));
	if (!rtn)
		return (NULL);
	while (s1[i])
	{
		rtn[i] = s1[i];
		i++;
	}
	rtn[i] = '\0';
	return (rtn);
}
